function L=constructL(feas,feat,beta)
%fea=[feas';feat'];
%[row,col]=size(fea);
options= [];
options.NeighborMode = 'KNN';
options.k =5;%5;
options.WeightMode = 'Binary';
Ws=constructW(feas',options);
Wt=constructW(feat',options);
Ws=beta*Ws;
Wt=beta*Wt;
options1 = [];
options1.NeighborMode = 'KNN';
options1.k =2;%2;
[Wst,Wts]=constructComW(feas',feat',options1);
%Wts=constructComW(feat',feas',options1);
W1=[Ws Wst];
W2=[Wts Wt];
W=[W1;W2];
DCols = full(sum(W,2));
D = spdiags(DCols,0,speye(size(W,1)));
L = D - W;
